/* a4_class_citzens_office.dart - Bürgerbüro
Entwickle eine Klasse für ein Bürgerbüro, die verschiedene
Dienstleistungen für Bürger anbietet. Diese Klasse soll die
zuvor erstellte Klasse für Person verwenden, um Bürgern
bestimmte Services zur Verfügung zu stellen.
Anforderungen
Ummeldung: Implementiere eine Methode, um die Adresse eines
Bürgers zu ändern. Diese stellt den Ummeldungsservice des
Bürgerbüros dar.
Aufenthaltsverwaltung: Die Klasse soll in der Lage sein,
zu verfolgen, welche Bürger sich aktuell im Bürgerbüro aufhalten.
Implementiere Methoden, durch die Bürger das Bürgerbüro betreten
oder verlassen.
Informationsabfrage: Füge eine Methode hinzu, die Informationen
darüber bereitstellt, welche und wie viele Bürger sich gerade
im Bürgerbüro befinden.
Hinweise
Nutze die zuvor erstellte Person-Klasse. Stelle sicher, dass jede
Person, die das Bürgerbüro betritt oder verlässt, als Objekt der
Person-Klasse gehandhabt wird.
Überlege dir, wie du die Liste der Personen im Bürgerbüro
speichern und verwalten kannst.
Zusätzlich zur Implementierung der Klasse Bürgerbüro sollten
auch mehrere Objekte dieser Klasse erstellt werden.
*/
